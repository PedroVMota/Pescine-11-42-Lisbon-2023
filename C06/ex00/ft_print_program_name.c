#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		ft_putchar(str[i]);
}

int	main(int argc, char **argv)
{
	if (argc >= 0)
		ft_putstr(argv[0]);
	write(1, "\n", 1);
	return (0);
}
