#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int	main(int tt, char **arg)
{
	int	i;

	i = tt - 1;
	while (i > 0)
	{
		ft_putstr(arg[i]);
		i--;
	}
	return (0);
}
